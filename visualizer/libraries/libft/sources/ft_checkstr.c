#include "libft.h"

int		ft_checkstr(char *s1, char *s2)
{
	int i;
	int j;

	i = 0;
	while (s1[i] != 0)
	{
		j = 0;
		while (s2[j] != 0 && s1[i] != s2[j])
			j++;
		if (s1[i] == s2[j])
			i++;
		else
			return (1);
	}
	return (0);
}
